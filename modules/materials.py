"""
materials.py — curated material library with rich phonological annotations.

Annotation types per word:
  stress    — stressed syllable marker (large dot above)
  weak      — weak/reduced form, label = phonetic reduction e.g. "wk /ə/"
  link      — linking, label = "linking →" or "← word"
  fall      — falling tone on this word ↘
  rise      — rising tone ↗
  fall_rise — fall-rise ↗↘
  nuclear   — nuclear stress (★, most prominent syllable in phrase)
  pause     — silence/pause marker (|), attach to preceding word

Each segment also has:
  pauses: list of word indices AFTER which a pause occurs
  intonation: "fall" | "rise" | "fall_rise" (sentence-final contour)
"""

import re

TAGS = {
    "stress":     {"label": "Stress Pattern", "zh": "重音模式",
                   "color": "#92400E", "bg": "#FFFBEB", "border": "#FDE68A"},
    "link":       {"label": "Linking",        "zh": "连读",
                   "color": "#1D4ED8", "bg": "#DBEAFE", "border": "#93C5FD"},
    "weak":       {"label": "Weak Form",      "zh": "弱读",
                   "color": "#5B21B6", "bg": "#EDE9FE", "border": "#C4B5FD"},
    "intonation": {"label": "Intonation",     "zh": "语调",
                   "color": "#065F46", "bg": "#ECFDF5", "border": "#6EE7B7"},
    "rhythm":     {"label": "Rhythm",         "zh": "节奏",
                   "color": "#9D174D", "bg": "#FCE7F3", "border": "#F9A8D4"},
    "omission":   {"label": "Omission",       "zh": "漏读",
                   "color": "#374151", "bg": "#F3F4F6", "border": "#D1D5DB"},
    "insertion":  {"label": "Insertion",      "zh": "多读",
                   "color": "#374151", "bg": "#F3F4F6", "border": "#D1D5DB"},
    "nuclear":    {"label": "Nuclear Stress", "zh": "核心重音",
                   "color": "#854F0B", "bg": "#FEF3C7", "border": "#FCD34D"},
    "vowel":      {"label": "Vowel Quality",  "zh": "元音质量",
                   "color": "#0369A1", "bg": "#E0F2FE", "border": "#7DD3FC"},
    "consonant":  {"label": "Consonant",      "zh": "辅音",
                   "color": "#4D7C0F", "bg": "#ECFCCB", "border": "#A3E635"},
}


def _seg(idx, text, start, end, words, cue_en, cue_zh, intonation="fall", pauses=None):
    """
    words: list of dicts {w, anns:[]}
      ann types: stress | weak(label) | link(label) | fall | rise | fall_rise | nuclear | pause
    intonation: sentence-final contour — fall | rise | fall_rise
    pauses: list of word indices after which a pause mark appears
    """
    return {
        "idx":         idx,
        "text":        text,
        "ts":          {"start": start, "end": end},
        "words":       words,
        "cue_en":      cue_en,
        "cue_zh":      cue_zh,
        "intonation":  intonation,
        "pauses":      pauses or [],
        # legacy flat annotations kept for backward compat
        "annotations": _flatten_anns(words),
    }


def _flatten_anns(words):
    out = []
    for wd in words:
        for a in wd.get("anns", []):
            t = a["type"]
            tag_key = t if t in TAGS else "stress"
            out.append({
                "word":  wd["w"],
                "type":  tag_key,
                "label": a.get("label", TAGS.get(tag_key, {}).get("label", t)),
                "zh":    TAGS.get(tag_key, {}).get("zh", ""),
            })
    return out


def _w(word, *anns):
    """Word with annotations. Each ann is a dict {type, label?}"""
    return {"w": word, "anns": list(anns)}


def _stress():    return {"type": "stress"}
def _nuclear():   return {"type": "nuclear"}
def _fall():      return {"type": "fall"}
def _rise():      return {"type": "rise"}
def _fall_rise(): return {"type": "fall_rise"}
def _weak(label): return {"type": "weak", "label": label}
def _link(label): return {"type": "link", "label": label}

